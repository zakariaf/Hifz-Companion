// =====================================================================================
// 3. THE CONNECTION: WAL + synchronous=FULL + foreign_keys=ON, and the OPT-IN cipher guard
// -------------------------------------------------------------------------------------
// The mandatory floor (§3) is WAL crash-safety + transactional integrity. synchronous=FULL,
// not NORMAL, because a teacher sign-off must survive power loss (§3). Encryption is OPTIONAL,
// off by default (§5): the PRAGMA cipher; liveness guard refuses a silently-plaintext store.
// =====================================================================================

LazyDatabase openConnection({String? encryptionKeyHex}) {
  return LazyDatabase(() async {
    final dir = await getApplicationDocumentsDirectory();
    final file = File(p.join(dir.path, 'hifz.sqlite'));

    return NativeDatabase.createInBackground(
      file,
      // setup runs on the raw sqlite3 handle BEFORE drift touches the DB.
      setup: (raw) {
        // --- OPT-IN encryption (§5). Present only in the encryption-enabled flavor. ---
        if (encryptionKeyHex != null) {
          // Raw-key BLOB literal: full-entropy key, skips PBKDF2.
          raw.execute('PRAGMA key = "x\'$encryptionKeyHex\'";');
          // HARD GUARD: refuse to operate on a plaintext store that only LOOKS encrypted.
          if (raw.select('PRAGMA cipher;').isEmpty) {
            throw StateError(
              'Encryption build active but cipher is not live; refusing to open.',
            );
          }
        }
        // --- Mandatory crash-safety floor (§1, §3). ---
        raw.execute('PRAGMA journal_mode = WAL;'); // crash-safe append-only journal
        raw.execute('PRAGMA synchronous = FULL;'); // durable across power loss
        raw.execute('PRAGMA foreign_keys = ON;'); // SQLite leaves FKs OFF by default
        raw.execute('PRAGMA busy_timeout = 5000;'); // wait, don't throw, on brief contention
      },
    );
  });
}

// =====================================================================================
// 4. THE APPEND-ONLY DAO + THE commitReview SINGLE-TRANSACTION REPOSITORY BODY
// -------------------------------------------------------------------------------------
// review_log is APPEND-ONLY: this DAO exposes INSERT only — NO update/delete method, ever (§2).
// Every review is exactly ONE db.transaction; a review is several writes, all-or-nothing (§3).
// await EVERY query inside the transaction — a missing await runs after the tx closes => data
// loss (§3, the await footgun). The Future resolves only after the durable commit; the Riverpod
// controller (eng-create-riverpod-store) republishes AFTER — persist-before-publish.
// =====================================================================================

@DriftAccessor(tables: [ReviewLog, Cards, LineBlocks, ConfusionEdges])
class ReviewDao extends DatabaseAccessor<HifzDatabase> with _$ReviewDaoMixin {
  ReviewDao(super.db);

  /// Persist one review atomically. Returns only after the durable commit.
  ///
  /// `ReviewOutcome` is produced by the engine + grading pipeline
  /// (domain-scheduling-engine-rules, domain-grading-pipeline); this layer only persists it.
  Future<void> commitReview(ReviewOutcome r) {
    return db.transaction(() async {
      // 1. APPEND the immutable audit row (NEVER updated/deleted — the *sanad* record).
      await into(reviewLog).insert(r.logRow);

      // 2. UPDATE the card's engine state (D, S, due_at as a CalendarDate serial day, flags).
      await (update(cards)
            ..where((c) =>
                c.profileId.equals(r.profileId) & c.pageId.equals(r.pageId)))
          .write(r.cardUpdate);

      // 3. LAZILY create line-blocks for a repeatedly-lapsing page (if any).
      if (r.newLineBlocks.isNotEmpty) {
        await batch((b) => b.insertAll(lineBlocks, r.newLineBlocks));
      }

      // 4. BUMP mutashābihāt confusion edges (domain-mutashabihat-system), if a swap occurred.
      for (final edge in r.confusionBumps) {
        await into(confusionEdges).insertOnConflictUpdate(edge);
      }
      // Every query above is awaited; when this Future completes every row is durably on disk.
    });
    // TODO: do NOT add an UPDATE/DELETE method on review_log — append-only is enforced
    //       by the ABSENCE of those methods (§2). The only bulk touch is export / erase
    //       (domain-backup-format, PRD §16).
  }
}

