class StudyNotes extends Table {
  // UUID primary key as TEXT (matches the §2 id convention).
  TextColumn get noteId => text()();

  // FK to the owning profile; per-profile children cascade on profile delete (§2).
  // TODO: point at the real Profiles table reference.
  TextColumn get profileId =>
      text().references(Profiles, #profileId, onDelete: KeyAction.cascade)();

  // FK into the read-only reference table — fine to REFERENCE a reference table,
  // never to WRITE one (§2: reference tables are read-only by construction).
  IntColumn get pageId => integer().references(Pages, #pageId)();

  // Enumerable column constrained with a CHECK (... IN (...)) — not validated in Dart (§2).
  // TODO: replace with the real domain enum's wire values.
  TextColumn get kind =>
      text().check(kind.isIn(const ['anchor', 'reminder', 'correction']))();

  // SCHEDULING DAY: a CalendarDate serial integer, NULL until first relevant.
  // NEVER a DateTime here — an instant column reintroduces the DST off-by-one (§2 pitfalls).
  IntColumn get dueAt => integer().nullable()();

  // A true event instant — stored UTC ISO-8601 TEXT (§2).
  TextColumn get createdAtUtc => text()();

  // Small, schema-shaped, decode-validated JSON only — never Quran text or health facts (§2).
  TextColumn get payloadJson => text().nullable()();

  @override
  Set<Column> get primaryKey => {noteId};

  // STRICT + extra table-level invariants. Mirror the engine invariant defended in §2:
  // a memorized card always has a due_at — here, an example range/null guard.
  @override
